# 오행(五行) 쉽게 보기

세상 만물을 5가지로 분류한 거예요:
- **목(木)**: 나무, 봄, 성장, 간/담
- **화(火)**: 불, 여름, 활발, 심장/소장
- **토(土)**: 흙, 환절기, 중재, 위장/비장
- **금(金)**: 쇠, 가을, 결실, 폐/대장
- **수(水)**: 물, 겨울, 저장, 신장/방광

## 상생(서로 도와주는 관계)
- 목 → 화: 나무가 불을 키움
- 화 → 토: 불이 타면 재(흙)가 됨
- 토 → 금: 흙에서 금속이 나옴
- 금 → 수: 금속에서 물이 맺힘
- 수 → 목: 물이 나무를 키움

## 상극(서로 제어하는 관계)
- 목 → 토: 나무가 흙을 뚫음
- 화 → 금: 불이 금속을 녹임
- 토 → 수: 흙이 물을 막음
- 금 → 목: 도끼가 나무를 자름
- 수 → 화: 물이 불을 끔

