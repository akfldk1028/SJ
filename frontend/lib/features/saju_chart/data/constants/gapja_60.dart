/// 60갑자 조합 인덱스 계산
/// 천간 인덱스와 지지 인덱스로부터 60갑자 인덱스 계산
int getGapjaIndex(int ganIndex, int jiIndex) {
  // 천간 10개, 지지 12개의 최소공배수 60
  for (int i = 0; i < 60; i++) {
    if (i % 10 == ganIndex && i % 12 == jiIndex) {
      return i;
    }
  }
  return 0;
}

/// 60갑자 문자열로부터 인덱스 찾기
int findGapjaIndex(String gapja) {
  final index = gapja60.indexOf(gapja);
  return index >= 0 ? index : 0;
}
