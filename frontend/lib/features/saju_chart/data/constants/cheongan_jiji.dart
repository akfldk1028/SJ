/// 천간지지 상수
/// 사주팔자 계산에 사용되는 천간(10개)과 지지(12개) 정의

/// 천간 (天干) - 10개
/// 갑, 을, 병, 정, 무, 기, 경, 신, 임, 계
const List<String> cheongan = [
  '갑', '을', '병', '정', '무',
  '기', '경', '신', '임', '계',
];

/// 지지 (地支) - 12개
/// 자, 축, 인, 묘, 진, 사, 오, 미, 신, 유, 술, 해
const List<String> jiji = [
  '자', '축', '인', '묘', '진', '사',
  '오', '미', '신', '유', '술', '해',
];

/// 천간 오행 매핑
const Map<String, String> cheonganOheng = {
  '갑': '목', '을': '목',
  '병': '화', '정': '화',
  '무': '토', '기': '토',
  '경': '금', '신': '금',
  '임': '수', '계': '수',
};

/// 지지 오행 매핑
const Map<String, String> jijiOheng = {
  '인': '목', '묘': '목',
  '사': '화', '오': '화',
  '진': '토', '술': '토', '축': '토', '미': '토',
  '유': '금',  // '신'은 천간에 이미 있음 (辛vs申 - 다른 한자지만 같은 한글)
  '해': '수', '자': '수',
};

/// 오행 조회 함수
String? getOheng(String char, {bool isCheongan = true}) {
  if (isCheongan) {
    return cheonganOheng[char];
  }
  return jijiOheng[char];
}

